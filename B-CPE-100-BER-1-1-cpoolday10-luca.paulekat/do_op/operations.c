int add(int nb1, int nb2)
{
    int res = nb1 + nb2;
    return (res);
}

int subtract(int nb1, int nb2)
{
    int res = nb1 - nb2;
    return (res);
}

int multiply(int nb1, int nb2)
{
    int res = nb1 * nb2;
    return (res);
}

int divide(int nb1, int nb2)
{
    int res = nb1 / nb2;
    return (res);
}

int modulo(int nb1, int nb2)
{
    int res = nb1 % nb2;
    return (res);
}
